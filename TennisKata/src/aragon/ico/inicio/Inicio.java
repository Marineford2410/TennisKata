    public void narracion(){
        if (!duece) {
            if (game.getServidor().getPuntos() > game.getReceptor().getPuntos()) {
                jLComentarios.setText(game.getServidor().getNombre() + " va ganando");
            }
            else if (game.getReceptor().getPuntos() > game.getServidor().getPuntos()) {
                jLComentarios.setText(game.getReceptor().getNombre() + " va ganando");
            }
            else {
                if (game.hayDeuce()) {
                    jLComentarios.setText("***** DEUCE *****");
                }
                else if (game.getServidor().getPuntos() == game.getReceptor().getPuntos()) {
                    jLComentarios.setText("El marcador se empató");
                }
            }
        }
        else{
            if (game.getServidor().getDeuce().equals("A")) {
                jLComentarios.setText("Punto para " + game.getServidor().getNombre());
            }
            else if(game.getReceptor().getDeuce().equals("A")){
                jLComentarios.setText("Punto para " + game.getReceptor().getNombre());
            }
        }
        
        if (game.getServidor().getGano()) {
            jLComentarios.setText("Juego finalizado. Ganador " + game.getServidor().getNombre());
        }
        else if (game.getReceptor().getGano()) {
            jLComentarios.setText("Juego finalizado. Ganador " + game.getReceptor().getNombre());
        }
    }

