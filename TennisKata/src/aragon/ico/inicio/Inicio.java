    private void jBServidorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBServidorActionPerformed
        if (!duece) {
            game.ronda(0);
            jLServidorPuntos.setText(String.valueOf(game.getServidor().getPuntos()));
            narracion();
        }
        else{
            game.deuce(game.getServidor(), game.getReceptor());
            jLServidorPuntos.setText(game.getServidor().getDeuce());
            jLReceptorPuntos.setText(String.valueOf(game.getReceptor().getPuntos()));
            narracion();
        }
        
        if (game.hayDeuce()) {
            duece = true;
        }
        
        if (game.getServidor().getGano()) {
            jBReceptor.setEnabled(false);
            jBServidor.setEnabled(false);
        }
        
        round++;
        jLRound.setText("ROUND " + round);
    }//GEN-LAST:event_jBServidorActionPerformed

